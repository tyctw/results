create table public.admissions (
  id bigint generated by default as identity primary key,
  year text,
  region text,
  school_name text,
  department text,
  ticket_number text,
  student_name text,
  gender text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 設定安全性原則 (Row Level Security)
alter table public.admissions enable row level security;

-- 允許所有人讀取 (供前端查詢)
create policy "Allow public read access"
  on public.admissions for select
  using ( true );

-- 注意：以下已移除 public 的 insert, update, delete 權限以提升系統安全性
-- 請使用包含 ADMIN_PASSWORD 的後端 API 來進行新增與刪除操作
-- 如果需要透過 Supabase Dashboard 手動修改資料，請確保您具有管理員權限


-- 管理員設定表
create table public.admin_settings (
  id integer primary key default 1,
  admin_password text not null
);

-- 設定安全性原則
alter table public.admin_settings enable row level security;

-- 預設密碼 (可登入後修改)
insert into public.admin_settings (id, admin_password) values (1, 'admin123') on conflict (id) do nothing;

-- 資料庫備份表 (保留最近3筆)
create table public.database_backups (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  file_name text not null,
  backup_data jsonb not null
);

-- 設定安全性原則
alter table public.database_backups enable row level security;
